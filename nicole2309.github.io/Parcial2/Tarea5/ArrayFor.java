public class ArrayFor {

    public static void main(String[] arg) {
   
   // create an array 
   int[] age = {12, 4, 5, 6, 3, 8, 2, 5, 7, 1, 6, 3, 8};
   
   
   //loop throgh the array 
   // using for loop
   System.out.println("Using for Loop: ");
   for(int i = 0; i < age.length; i ++) {
       System.out.println(age[i]) ;
      }
   }
   }